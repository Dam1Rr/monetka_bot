-- ================================================================
-- 1. МТС / Билайн / Мегафон / Теле2
--    Убираем из Транспорт (МТС в Кыргызстане = оператор, не метро)
--    Убираем из Интернет (V5 ошибочно положил туда)
--    Оставляем только в: Подписки/Мобильная связь
-- ================================================================
DELETE FROM subcategory_keywords
WHERE keyword IN ('мтс','билайн','мегафон','теле2','beeline','megafon','o!','мегаком')
  AND subcategory_id IN (
    SELECT s.id FROM subcategories s
                         JOIN categories c ON s.category_id = c.id
    WHERE c.name IN ('Транспорт') OR s.name IN ('Интернет','Общественный транспорт')
);

-- ================================================================
-- 2. "баланс" — слишком общее, убираем из Коммуналка
